/**
 * @brief Пятиточечный дискриминатор, МНК-гауссова аппроксимация (LSQ Gaussian 5pt).
 *
 * Аппроксимирует 5 равноотстоящих отсчётов гауссианом методом наименьших квадратов:
 * @f[
 *   y = A_{max} \exp\!\bigl(-a\,(x - x_0)^2\bigr)
 * @f]
 *
 * Алгоритм:
 *   1. Логарифмирование: @f$ z_i = \ln A_i @f$
 *   2. МНК-параболическая аппроксимация @f$ z(x) = a\,x^2 + b\,x + c @f$
 *      по 5 равноотстоящим точкам (переопределённая система 5 уравнений, 3 неизвестных)
 *   3. Вершина параболы: @f$ x_e = -b / (2a) @f$
 *
 * Формулы МНК для нормированных координат @f$ \{-2, -1, 0, 1, 2\} @f$:
 * @f[
 *   a = \frac{2z_1 - z_2 - 2z_3 - z_4 + 2z_5}{14}
 * @f]
 * @f[
 *   b = \frac{-2z_1 - z_2 + z_4 + 2z_5}{10}
 * @f]
 * @f[
 *   x_e = x_3 + h \cdot \frac{-b}{2a}
 * @f]
 *
 * Преимущества перед discr3ea:
 *   - Устойчивость к шуму за счёт сглаживания (5 точек → 3 параметра)
 *   - Гауссова модель хорошо приближает sinc(x) вблизи пика
 *
 * @warning Все амплитуды должны быть строго положительны (@f$ A_i > 0 @f$).
 * @warning Точки должны быть равноотстоящими (постоянный шаг h).
 *
 * @param A    массив из 5 амплитуд [A1, A2, A3, A4, A5], центральная = A[2]
 * @param x    массив из 5 координат [x1, x2, x3, x4, x5], равноотстоящие
 * @param xe   [out] оценка координаты @f$ x_e @f$
 * @return     EXIT_SUCCESS (0) при успехе, EXIT_FAILURE (1) при ошибке
 *             (при ошибке *xe содержит приближённое значение)
 */
int discr5ea(const double A[5], const double x[5], double *xe);

/**
 * @brief Пятиточечный дискриминатор, МНК-параболическая аппроксимация (LSQ Parabolic 5pt).
 *
 * Аналогичен discr5ea, но работает напрямую с амплитудами (без логарифмирования).
 *
 * Формулы МНК для нормированных координат @f$ \{-2, -1, 0, 1, 2\} @f$:
 * @f[
 *   a = \frac{2A_1 - A_2 - 2A_3 - A_4 + 2A_5}{14}
 * @f]
 * @f[
 *   b = \frac{-2A_1 - A_2 + A_4 + 2A_5}{10}
 * @f]
 *
 * @param A    массив из 5 амплитуд [A1, A2, A3, A4, A5]
 * @param x    массив из 5 координат [x1, x2, x3, x4, x5], равноотстоящие
 * @param xe   [out] оценка координаты @f$ x_e @f$
 * @return     EXIT_SUCCESS (0) при успехе, EXIT_FAILURE (1) при ошибке
 */
int discr5qa(const double A[5], const double x[5], double *xe);


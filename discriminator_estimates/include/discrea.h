/**
 * @brief Трехточечный дискриминатор, экспоненциальная аппроксимация (Exponential).
 *
 * Аппроксимирует отсчёты ДН функцией:
 * @f[
 *   y = A_{max} \exp\!\bigl(-a\,(x - x_0)^2\bigr)
 * @f]
 *
 * Алгоритм: логарифмирование амплитуд @f$ z_i = \ln A_i @f$,
 * аппроксимация параболой @f$ z(x) = \alpha + \beta x + \gamma x^2 @f$
 * и нахождение вершины:
 * @f[
 *   x_e = \frac{\alpha}{2\,\beta}
 * @f]
 * где @f$ \alpha @f$ и @f$ \beta @f$ вычисляются из системы уравнений
 * по трём логарифмированным амплитудам.
 *
 * @warning Все амплитуды должны быть строго положительны (@f$ A_i > 0 @f$).
 *          Максимум должен лежать в центре (выпуклость).
 *
 * @param A1  амплитуда первой точки (должна быть > 0)
 * @param A2  амплитуда второй точки (должна быть > 0)
 * @param A3  амплитуда третьей точки (должна быть > 0)
 * @param x1  координата первой точки
 * @param x2  координата второй точки
 * @param x3  координата третьей точки
 * @param xe  [out] оценка координаты @f$ x_e @f$
 * @return    EXIT_SUCCESS (0) при успехе, EXIT_FAILURE (1) при ошибке
 *            (при ошибке *xe содержит приближённое значение)
 */
int discr3ea(double A1, double A2, double A3,
             double x1, double x2, double x3, double* xe);

/**
 * @brief Уточнение амплитуды по экспоненциальной аппроксимации.
 *
 * По двум точкам и найденной координате @f$ x_e @f$ вычисляет
 * уточнённую амплитуду в точке @f$ x_e @f$:
 * @f[
 *   y_e = A_2 \cdot \exp\!\bigl(a_0 \cdot (x_2 - x_e)^2\bigr)
 * @f]
 * где коэффициент @f$ a_0 @f$ определяется из соотношения:
 * @f[
 *   a_0 = \frac{\ln A_1 - \ln A_2}
 *              {2\,x_e\,(x_1 - x_2) - x_1^2 + x_2^2}
 * @f]
 *
 * @param A1  амплитуда первой точки (должна быть > 0)
 * @param A2  амплитуда второй точки (должна быть > 0)
 * @param x1  координата первой точки
 * @param x2  координата второй точки
 * @param xe  вычисленная координата (результат discr3ea)
 * @return    уточнённое значение амплитуды @f$ y_e @f$;
 *            при @f$ A_1 < \varepsilon @f$ или @f$ A_2 < \varepsilon @f$
 *            возвращает 0.0
 */
double discr3eaY(double A1, double A2, double x1, double x2, double xe);


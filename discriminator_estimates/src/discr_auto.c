/**
 * @file    discr_auto.c
 * @author  Кодо (AI Assistant)
 * @date    2026-04-02
 * @brief   Автоматический дискриминатор с экстраполяцией.
 *
 * Реализует стратегию автопереключения на основе результатов
 * аналитического исследования (analysis/REPORT.md):
 *
 *   Нормальный случай (есть перегиб):
 *     EA → QA fallback
 *     EA даёт MAE = 0.005 (0.5% шага сетки)
 *
 *   Монотонный случай (пик за сеткой):
 *     E2 (Гауссова экстраполяция)
 *     E2 даёт MAE = 0.148, что в 2-3 раза лучше прямого EA
 *
 * E2 — это тот же алгоритм что и EA (логарифмирование + парабола),
 * но БЕЗ проверки на выпуклость и с расширенным clipping (2× вместо 0.5×).
 * Это позволяет вершине параболы оказаться далеко за сеткой — что и нужно.
 */

#include "discr_auto.h"
#include "discrea.h"
#include "discrqa.h"

#include <math.h>
#include <float.h>
#include <stdlib.h>

/* ═══════════════════════════════════════════════════════════════════ */
/*  discr_is_monotonic                                                 */
/* ═══════════════════════════════════════════════════════════════════ */

int discr_is_monotonic(double A1, double A2, double A3)
{
  /*
   * Монотонные = все убывают ИЛИ все возрастают.
   * Это значит, что среди A1, A2, A3 нет локального максимума,
   * т.е. пик ДН находится за пределами сетки из 3 отсчётов.
   *
   * Примеры:
   *   A1=0.8, A2=0.6, A3=0.3 → убывают → монотонные (пик слева)
   *   A1=0.3, A2=0.6, A3=0.8 → возрастают → монотонные (пик справа)
   *   A1=0.6, A2=0.9, A3=0.7 → перегиб → НЕ монотонные (пик внутри)
   */
  return (A1 >= A2 && A2 >= A3) ||  /* убывают (или равны) */
         (A1 <= A2 && A2 <= A3);     /* возрастают (или равны) */
}

/* ═══════════════════════════════════════════════════════════════════ */
/*  discr3_extrap_gauss — Гауссова экстраполяция (метод E2)           */
/* ═══════════════════════════════════════════════════════════════════ */

/**
 * @brief Структура для сортировки пар (амплитуда, координата) по x.
 */
typedef struct {
  double a;
  double x;
} ax_pair_t;

static int cmp_by_x(const void *p1, const void *p2)
{
  const ax_pair_t *a = (const ax_pair_t *)p1;
  const ax_pair_t *b = (const ax_pair_t *)p2;
  if (a->x < b->x) return -1;
  if (a->x > b->x) return  1;
  return 0;
}

int discr3_extrap_gauss(double A1, double A2, double A3,
                        double x1, double x2, double x3,
                        double *xe)
{
  ax_pair_t pts[3];
  double a1, a2, a3, f1, f2, f3;
  double z1, z2, z3;
  double a_coef, b_coef;
  double span;

  /*
   * Шаг 1. Проверка: все амплитуды > 0
   * (нужно для логарифмирования)
   */
  if (A1 < DBL_EPSILON || A2 < DBL_EPSILON || A3 < DBL_EPSILON) {
    *xe = x2;
    return EXIT_FAILURE;
  }

  /* Все амплитуды одинаковы → невозможно определить направление */
  if (fabs(A1 - A2) < DBL_EPSILON && fabs(A2 - A3) < DBL_EPSILON) {
    *xe = x2;
    return EXIT_FAILURE;
  }

  /*
   * Шаг 2. Сортировка по координатам (x возрастает)
   * Это нужно чтобы формулы работали корректно
   * независимо от порядка входных аргументов.
   */
  pts[0].a = A1; pts[0].x = x1;
  pts[1].a = A2; pts[1].x = x2;
  pts[2].a = A3; pts[2].x = x3;
  qsort(pts, 3, sizeof(ax_pair_t), cmp_by_x);

  a1 = pts[0].a; f1 = pts[0].x;
  a2 = pts[1].a; f2 = pts[1].x;
  a3 = pts[2].a; f3 = pts[2].x;

  /*
   * Шаг 3. НЕ проверяем выпуклость!
   *
   * Это ГЛАВНОЕ отличие от discr3ea:
   * - discr3ea проверяет что a2 > a1 и a2 > a3 (перегиб)
   *   и возвращает FAILURE для монотонных данных
   * - Мы РАЗРЕШАЕМ монотонные данные, потому что
   *   вершина параболы в log-пространстве покажет
   *   КУДА и НАСКОЛЬКО далеко экстраполировать пик.
   */

  /*
   * Шаг 4. Логарифмирование
   *
   * Гауссиан y = A*exp(-a*(x-x0)^2) после логарифмирования
   * становится параболой: ln(y) = ln(A) - a*(x-x0)^2
   *
   * Парабола в log-пространстве → вершина = x0 (положение пика).
   */
  z1 = log(a1);
  z2 = log(a2);
  z3 = log(a3);

  /*
   * Шаг 5. Коэффициенты параболы
   *
   * Парабола z(x) через 3 точки (f1,z1), (f2,z2), (f3,z3):
   *   a_coef = z1*(f2²-f3²) + z2*(f3²-f1²) + z3*(f1²-f2²)
   *   b_coef = z1*(f2-f3)   + z2*(f3-f1)    + z3*(f1-f2)
   *
   * Вершина: xe = 0.5 * a_coef / b_coef
   */
  a_coef = z1 * (f2 * f2 - f3 * f3)
         + z2 * (f3 * f3 - f1 * f1)
         + z3 * (f1 * f1 - f2 * f2);

  b_coef = z1 * (f2 - f3)
         + z2 * (f3 - f1)
         + z3 * (f1 - f2);

  /* b_coef ≈ 0 → парабола вырожденная, направление неопределено */
  if (fabs(b_coef) < DBL_EPSILON) {
    /* Экстраполируем в сторону наибольшей амплитуды */
    if (a3 > a1)
      *xe = f3 + 0.5 * (f3 - f1);
    else
      *xe = f1 - 0.5 * (f3 - f1);
    return EXIT_FAILURE;
  }

  /* Вершина параболы в log-пространстве = оценка положения пика */
  *xe = 0.5 * a_coef / b_coef;

  /*
   * Шаг 6. Ограничение вылета (clipping)
   *
   * Расширенный диапазон: ±2 × размер_сетки (вместо ±0.5× в discr3ea).
   * Это позволяет экстраполировать дальше, но не бесконечно далеко.
   *
   * Почему 2×? Из исследования: при вылете > 2× шага ошибка
   * экстраполяции уже не уменьшается, а растёт.
   */
  span = f3 - f1;  /* = 2 × шаг_сетки для симметричной сетки */

  if (*xe > f3 + span) {
    *xe = f3 + span;
    return EXIT_FAILURE;
  }
  if (*xe < f1 - span) {
    *xe = f1 - span;
    return EXIT_FAILURE;
  }

  return EXIT_SUCCESS;
}

/* ═══════════════════════════════════════════════════════════════════ */
/*  discr3_auto — автоматический дискриминатор                        */
/* ═══════════════════════════════════════════════════════════════════ */

int discr3_auto(double A1, double A2, double A3,
                double x1, double x2, double x3,
                double *xe)
{
  int ret;

  /*
   * Ветка 1: МОНОТОННЫЕ данные (пик за сеткой)
   *
   * Когда A1 ≥ A2 ≥ A3 или A1 ≤ A2 ≤ A3, среди 3 отсчётов
   * нет перегиба — пик ДН находится за пределами сетки.
   *
   * Стандартные дискриминаторы в этом случае:
   *   CG: MAE = 0.46 (почти случайная оценка)
   *   QA: MAE = 0.32, MaxErr = 1.23 (парабола улетает!)
   *   EA: возвращает FAILURE, ставит xe = краевой отсчёт
   *
   * Гауссова экстраполяция (E2): MAE = 0.148
   *   → в 2-3 раза лучше любого стандартного метода!
   */
  if (discr_is_monotonic(A1, A2, A3)) {
    ret = discr3_extrap_gauss(A1, A2, A3, x1, x2, x3, xe);
    return (ret == EXIT_SUCCESS) ? 2 : 3;
    /* 2 = E2 сработала, 3 = E2 не смогла (нули, вырожденность) */
  }

  /*
   * Ветка 2: НОРМАЛЬНЫЕ данные (пик внутри сетки)
   *
   * EA (экспоненциальный): MAE = 0.005 в нормальной зоне
   *   Лучший метод! Использует log-параболу, точнее QA.
   *
   * Если EA вернёт FAILURE (вогнутость, нули), переходим на QA.
   * QA: MAE = 0.006 — почти так же точен, но без ограничений
   *     на знак амплитуд и выпуклость.
   */
  ret = discr3ea(A1, A2, A3, x1, x2, x3, xe);

  if (ret == EXIT_SUCCESS) {
    return 0;  /* EA справился — лучший результат */
  }

  /*
   * Ветка 3: FALLBACK на QA
   *
   * EA вернул FAILURE. Причины:
   *   - Вогнутость (минимум в центре вместо максимума)
   *   - Нулевые или отрицательные амплитуды
   *   - Числовая вырожденность
   *
   * QA не требует положительных амплитуд и не проверяет
   * выпуклость — работает всегда (кроме совсем вырожденных случаев).
   */
  *xe = discr3qa(A1, A2, A3, x1, x2, x3);
  return 1;  /* QA fallback */
}
